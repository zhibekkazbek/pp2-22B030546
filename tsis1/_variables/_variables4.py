# In the print() function, you output multiple variables, separated by a comma or "+":
x = 'Python'
y = 'is'
z = 'awesome'
print(x, y, z)
x = 'Python '
y = 'is '
z = 'awesome'
print(x + y + z)

x = 5
y = 10
print(x + y)

# The best way to output multiple variables in the print() function is to separate them with commas, 
# which even support different data types:
x = 5
y = 'John'
print(x, y)